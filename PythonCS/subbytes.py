def sub_bytes(state):
    # AES S-box
    s_box = [
        0x63, 0x7C, 0x77, 0x7B, 0xF2, 0x6B, 0x6F, 0xC5, 0x30, 0x01, 0x67, 0x2B, 0xFE, 0xD7, 0xAB, 0x76,
        0xCA, 0x82, 0xC9, 0x7D, 0xFA, 0x59, 0x47, 0xF0, 0xAD, 0xD4, 0xA2, 0xAF, 0x9C, 0xA4, 0x72, 0xC0,
        # ... (complete S-box here)
    ]

    # Apply S-box to each byte in the state
    for i in range(len(state)): #0-3
        for j in range(len(state[i])): #0-3
            state[i][j] = s_box[state[i][j]]

    return state

# Example usage
state = [
    [0x32, 0x88, 0x31, 0xe0],
    [0x43, 0x5a, 0x31, 0x37],
    [0xf6, 0x30, 0x98, 0x07],
    [0xa8, 0x8d, 0xa2, 0x34]
]

new_state = sub_bytes(state)
print(new_state)
